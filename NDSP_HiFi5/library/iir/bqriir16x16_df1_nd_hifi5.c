    // Save section's state elements.
    //
    Asxsr = AE_SEL16_7610(Asx, Asr);
    Bsxsr = AE_SEL16_7610(Bsx, Bsr);
    Csxsr = AE_SEL16_7610(Csx, Csr);
    Dsxsr = AE_SEL16_7610(Dsx, Dsr);
